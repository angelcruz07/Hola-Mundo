# Modulo Math operaciones matematicas
import math

print(round(1.3))  # redondear numeros
print(abs(2.4))  # valor absoluto
print(math.ceil(1.1))
print(math.floor(1.9999))
print(math.isnan(23))
print(math.pow(10, 3))
print(math.sqrt(0))

